


def sum(tall1, tall2):
    result= tall1+tall2
    return result

def subtraksjon(tall1, tall2):
    result = tall1-tall2
    return result

def multiplikasjon(tall1,tall2):
    result = tall1*tall2
    return result

def divisjon(tall1, tall2):
    result = tall1/tall2
    return result
